    private void jButtonAddWordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddWordActionPerformed
        // TODO add your handling code here:
        if (jTextFieldAddWord.getText().equals("")) return; //don't add word if empty
        if (!jButtonAddWord.isEnabled()) return; //if button is grayed out, stop (used for when pressing enter on text field)
        
        Object item = jComboBoxSelectList.getSelectedItem();
        DefaultListModel model = new DefaultListModel();
        String[] newList = new String[game.getAList(item.toString()).length + 1];
        newList[0] = item.toString();
        
        for(int i = 0; i < game.getAList(item.toString()).length; i++){
            
            model.addElement(game.getAList(item.toString())[i]);
            newList[i+1] = game.getAList(item.toString())[i];
            
        }
        
        model.addElement(jTextFieldAddWord.getText());
        newList[game.getAList(item.toString()).length] = jTextFieldAddWord.getText();
        
        System.out.println(item.toString());
        
        XMLParser theParser = new XMLParser();
        theParser.writeHangmanFile(newList, item.toString());
        
        jList1.setModel(model);
        jList1.setSelectedIndex(0);
        jTextFieldAddWord.setText("");
        game.setUp();
        
    }//GEN-LAST:event_jButtonAddWordActionPerformed

    private void jButtonRemoveWordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoveWordActionPerformed
        // TODO add your handling code here:
        
        Object item = jComboBoxSelectList.getSelectedItem();
        DefaultListModel model = new DefaultListModel();
        String[] newList = new String[game.getAList(item.toString()).length-1];
        newList[0] = item.toString();
        String selectedWord = jList1.getSelectedValue();
        int lengthModifier = 0;
        
        for(int i = 0; i < (game.getAList(item.toString()).length - 1); i++){
            
            if(!game.getAList(item.toString())[i].equals(selectedWord)){
                
                model.addElement(game.getAList(item.toString())[i]);
                newList[i+1-lengthModifier] = game.getAList(item.toString())[i];
            
            }else{
                
                lengthModifier++;
                
            }
            
        }
        
        XMLParser theParser = new XMLParser();
        theParser.writeHangmanFile(newList, item.toString());
        
        jList1.setModel(model);
        jList1.setSelectedIndex(0);
        game.setUp();
        
    }//GEN-LAST:event_jButtonRemoveWordActionPerformed

