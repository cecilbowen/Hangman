    //Takes the text of a button (the letter) and sends it to Hangman.java to see if user guessed a correct letter
    private void jButtonGuess(java.awt.event.ActionEvent evt) {
        if (game.isGameOver()) return; //don't let user click any letters if game over
        
        JButton button = (JButton) evt.getSource(); //gets the object (jButton) that is pressed
        String letterGuessed = evt.getActionCommand(); //gets the text of the button (the letter)
        
        //gray out the button
        button.setEnabled(false);
        
        //if correct letter guessed, green out
        if (game.takeTurn(letterGuessed)) {
            button.setOpaque(true);
            button.setBackground(Color.GREEN);
        } else redrawBody();
        
        //spaces out current word correctly o_O
        jLabelWord.setText(game.getRevealedWord().replaceAll(".(?=.)", "$0    ").trim());
        
        //when game is won/lost
        if (!game.gameStatus().equals("in progress")) {
            jLabelScore.setText("Score: "+game.getScore());
            if (game.gameStatus().equals("won")) {
                //Shows Continue/Quit Buttons
                jButtonContinue.setEnabled(true);
                jLabelResult.setText("Congratulations! You won!");
            } else {
                //Shows Quit Button Only
                jLabelResult.setText("Sorry! You lost!");
                jLabelWord.setText(game.getWord());
            }
        }
    }
    
